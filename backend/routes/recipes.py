# Переводы для сложности
DIFFICULTY_KK = {
    "easy": "Оңай",
    "medium": "Орташа",
    "hard": "Қиын",
}


def get_translated_recipe(recipe_dict, language):
    """Переводит рецепт на казахский язык"""
    if language != 'kk':
        return recipe_dict

    result = recipe_dict.copy()
    recipe_id = recipe_dict.get('id')

    # Если есть полный перевод рецепта
    if recipe_id in RECIPES_KK:
        kk_data = RECIPES_KK[recipe_id]
        result['name'] = kk_data.get('name', result['name'])
        result['tags'] = kk_data.get('tags', result['tags'])
        result['ingredients'] = kk_data.get('ingredients', result['ingredients'])
        result['steps'] = kk_data.get('steps', result['steps'])
    else:
        # Переводим только теги
        if result.get('tags'):
            result['tags'] = [TAGS_KK.get(tag, tag) for tag in result['tags']]

    # Переводим сложность
    if result.get('difficulty') in DIFFICULTY_KK:
        result['difficulty'] = DIFFICULTY_KK[result['difficulty']]

    return result


@recipes_bp.route('get', methods=['GET'])
@jwt_required()
def get_recipes():
    """Получить список рецептов с фильтрацией"""
    try:
        category = request.args.get('category')
        difficulty = request.args.get('difficulty')
        search = request.args.get('search')

        # Получаем язык
        language = request.headers.get('Accept-Language', 'ru')
        if ',' in language:
            language = language.split(',')[0]
        if '-' in language:
            language = language.split('-')[0]
        language = request.args.get('lang', language)

        query = Recipe.query

        if category and category != 'all':
            query = query.filter(Recipe.category == category)

        if difficulty and difficulty != 'all':
            query = query.filter(Recipe.difficulty == difficulty)

        if search:
            search_lower = f'%{search.lower()}%'
            query = query.filter(
                db.or_(
                    db.func.lower(Recipe.name).like(search_lower),
                    db.func.lower(Recipe.tags).like(search_lower)
                )
            )

        recipes = query.order_by(Recipe.id.asc()).all()

        # Переводим каждый рецепт
        translated_recipes = [
            get_translated_recipe(r.to_dict(), language)
            for r in recipes
        ]

        return jsonify({
            'recipes': translated_recipes,
            'count': len(translated_recipes)
        })

    except Exception as e:
        logger.error(f"[GET /recipes] Ошибка: {e}", exc_info=True)
        return jsonify({'error': 'Ошибка получения рецептов'}), 500


@recipes_bp.route('/<int:recipe_id>', methods=['GET'])
@jwt_required()
def get_recipe(recipe_id):
    """Получить конкретный рецепт"""
    try:
        # Получаем язык
        language = request.headers.get('Accept-Language', 'ru')
        if ',' in language:
            language = language.split(',')[0]
        if '-' in language:
            language = language.split('-')[0]
        language = request.args.get('lang', language)

        recipe = Recipe.query.get(recipe_id)

        if not recipe:
            return jsonify({'error': 'Рецепт не найден'}), 404

        translated_recipe = get_translated_recipe(recipe.to_dict(), language)

        return jsonify({'recipe': translated_recipe})

    except Exception as e:
        logger.error(f"[GET /recipes/{recipe_id}] Ошибка: {e}", exc_info=True)
        return jsonify({'error': 'Ошибка получения рецепта'}), 500


@recipes_bp.route('/generate', methods=['POST'])
@jwt_required()
def generate_recipe_chat():
    """Chat with AI to generate recipes from fridge products."""
    try:
        data = request.get_json()

        if not data:
            return jsonify({'error': 'Request body required'}), 400

        product_names = data.get('products', [])
        conversation_history = data.get('history', [])
        user_message = data.get('message', '')

        if not user_message:
            return jsonify({'error': 'Message is required'}), 400

        if not product_names:
            return jsonify({'error': 'Product list is required'}), 400

        for msg in conversation_history:
            if msg.get('role') not in ('user', 'assistant'):
                return jsonify({'error': 'Invalid history format'}), 400

        response_text = generate_recipe_response(
            product_names=product_names,
            conversation_history=conversation_history,
            user_message=user_message,
        )

        return jsonify({
            'response': response_text,
            'role': 'assistant',
        })

    except Exception as e:
        logger.error(f"[POST /recipes/generate] Error: {e}", exc_info=True)
        return jsonify({
            'error': 'Не удалось сгенерировать рецепт. Попробуйте ещё раз.'
        }), 500
